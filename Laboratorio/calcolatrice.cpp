#include <iostream>
using namespace std;

int main() {
    
    int a, b;
    
    
    cout << "Inserisci primo numero: ";
    cin >> a;
    cout << "Inserisci secondo numero: ";
    cin >> b;
    
    //somma
    int somma = a+b;
    
    cout << "La somma è: " << somma << endl;
    
    //sottrazione
    int sottrazione = a-b;
    
    cout << "La sottrazione è: " << sottrazione << endl;

    return 0;
}
